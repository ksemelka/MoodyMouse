/****************************************************************/
/*                    display four characters                   */
/*                this can be called during 1ms ISR             */
/****************************************************************/
void displayMatrix(const char *s)
{   
	int i, j;
	char const *ptr; //pointer for starting address of character s
	RS_0;   //select control register
	CE_0;	//enable data writing
	
	for(i=0; i<4; i++)
	{
		ptr = &fontTable[s[i]*5];
		for(j=0; j<5; j++)
		{
			while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET);
			SPI_I2S_SendData(SPI2, *ptr);
			ptr++;
		}//for j
	}//for i
			while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET);
			while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_BSY) == SET);
	CE_1;   //latch on
}

void displayErr(int err) {
	char str[5];
	snprintf(str, 5, "E%03d", err);
	
	displayMatrix(str);
}

void displayFloat(float f) {
	//a string with the format xx.x
	char str[5]; //need 5 because of null byte
	
	snprintf(str, 5, "%.1f", f);
	
	displayMatrix(str);
}

void displayInt(int i) {
	char str[5];
	
	snprintf(str, 5, "%04d", i);
	
	displayMatrix(str);
}

void displayMatrixScroll(char* str) {
	int i;
	
	for(i = 0; i < 3; i++) {
		if(str[i] == '\0') {
			displayMatrix(str);
			return;
		}
	}
	
	i = 0;
	while(str[i+3] != '\0') {
		displayMatrix(&str[i]);
		delay_ms(1000);
		i++;
	}
}


/****************************************************************/
/*                       clear screen                           */
/*                 write 4 spaces into screen                   */
/****************************************************************/
void clearScreen(void)
{   
	int i;
	RS_0;   //select control register
	CE_0;	//enable data writing
	
		for(i=0; i<20; i++)
		{
			while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET);
			SPI_I2S_SendData(SPI2, 0x00);
		}
			while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET);
			while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_BSY) == SET);
	CE_1;   //latch on
}



